CREATE DATABASE job_application_db;
USE job_application_db;


CREATE TABLE titles (
	title_id INT AUTO_INCREMENT PRIMARY KEY,
	title_name VARCHAR(10) NOT NULL
)


CREATE TABLE provinces (
	province_id INT PRIMARY KEY,
	province_name VARCHAR(100) NOT NULL
)


CREATE TABLE districts (
	district_id INT PRIMARY KEY,
	province_id INT NOT NULL,
	district_name VARCHAR(100) NOT NULL,
	CONSTRAINT fk_district_province FOREIGN KEY (province_id) REFERENCES provinces(province_id)
)


CREATE TABLE subdistricts (
	subdistrict_id INT PRIMARY KEY,
	district_id INT NOT NULL,
	subdistrict_name VARCHAR(100) NOT NULL,
	CONSTRAINT fk_subdistrict_district FOREIGN KEY (district_id) REFERENCES districts(district_id)
)


CREATE TABLE applications (
	application_id INT AUTO_INCREMENT PRIMARY KEY,
	title_id INT NOT NULL,
	first_name VARCHAR(100) NOT NULL,
	last_name VARCHAR(100) NOT NULL,
	phone_number VARCHAR(20) NOT Null,
	province_id INT NOT NULL,
	district_id INT NOT NULL,
	subdistrict_id INT NOT NULL,
	poto VARCHAR(255),
	created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	CONSTRAINT fk_application_title FOREIGN KEY (title_id) REFERENCES titles(title_id),
	CONSTRAINT fk_application_province FOREIGN KEY (province_id) REFERENCES provinces(province_id),
	CONSTRAINT fk_application_district FOREIGN KEY (district_id) REFERENCES districts(district_id),
	CONSTRAINT fk_application_subdistrict FOREIGN KEY (subdistrict_id) REFERENCES subdistricts(subdistrict_id)
);


