       t.title_name,            
       a.first_name,            
       a.last_name,             
       a.phone_number,          
       p.province_name,         
       d.district_name,         
       s.subdistrict_name,      
       a.poto                   
FROM applications a
JOIN titles t ON a.title_id = t.title_id
JOIN provinces p ON a.province_id = p.province_id
JOIN districts d ON a.district_id = d.district_id
JOIN subdistricts s ON a.subdistrict_id = s.subdistrict_id;


